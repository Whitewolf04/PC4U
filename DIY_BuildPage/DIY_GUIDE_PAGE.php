						<h2 id="stepguide">
							How to Put it All Together
						</h2>
						With the motherboard, there should be an instruction manual that lists and shows all the sockets for your PC's components. Make sure to consult it regularly while you install the other parts.
						<ol>
							<li>
								<h3>Installing the CPU</h3>
								The CPU has a side with a bunch of small pins. <span>Do not touch them, they are very fragile.</span><br>
								On your motherboard, there should be a processor socket. Unlock it, then align the processor with the golden triangle on its corner and the one on the motherboard, place it into the socket, and lock the processor into place.<br>
								Now that the processor is in place, put a dab of thermal paste onto the center of the processor on the silicon square.<br>
								Align the heat sink with the screws around the processor and lower it onto the paste, squishing it into a thin layer.<br>
								If thermal paste overflows over the edge of the processor, use isopropyl alcohol and a wipe to clean off all the thermal paste from the processor and start over.<br>
								Wait until it dries, then screw the heat sink into place. Once that's done, plug in the heat sink's cooling fan.
							</li>
							<li>
								<h3>Installing the Motherboard</h3>
								Line up the screw holes of the motherboard with those of your case, and screw it in.
							</li>
							<li>
								<h3>Installing the Power Supply</h3>
								The power supply is fitted and screwed into a square slot in the case.<br>
								<span>Make sure the power supply cables reach the motherboard.</span>
							</li>
							<li>
								<h3>Installing the GPU</h3>
								Plug the GPU into the PCIe slot in your motherboard.<br>
								The back of the GPU with the HDMI and DisplayPorts should also line up with the back of your case and fit with relative ease.<br>
								The power supply should have a cable that can be inserted into the GPU, the socket looks like it has six or eight holes.
							</li>
							<li>
								<h3>Installing Storage and Memory</h3>
								Beside the CPU, on the motherboard, there should be vertical sockets where you can insert your sticks of RAM for memory needs.<br>
								<span>Consult your motherboard's instruction manual for which slots to use.</span><br>
								For an HD or SSD, there should be an empty bay in the case where you can slot and screm them into place.<br>
								For an M.2 drive, there should be a slot on your motherboard where you can insert one.
							</li>
							<li>
								<h3>Managing the Cables</h3>
								Plug your motherboard into all the other components with their respective cables.<br>
								Hook up your power supply to all your other components, most importantly, to your SSD and motherboard.<br>
								Hook up your motherboard to its case, meaning specifically, the power buttons, audio plugs, and USB ports on the front of you case.<br>
								<span>Hook up computer fans to your case as well that then connect to your motherboard.</span><br>
								Your SSD also connects to your motherboard with its SATA cable.
							</li>
							<li>
								<h3>Install your Operating System</h3>
								Turn on your PC. If it starts up with no issues, then turn it off and plug in a flash drive with Windows on it.<br>
								Turn on your PC again and it should boot up with Windows automatically. Otherwise, set the drive as a boot device in your BIOS.
							</li>
							<li>
								<h3>Congratulations, you have built a PC.</h3>
								When it comes down to it, building a PC can seem like a very stressful and arduous task, but in reality, it is straight-forward and merely requires that you have patience and follow instructions.<br>
								Now that the long and hard work is over, your custom hand-built PC will open you up to a vastly bigger digital realm of possibilities and utilities than that of your standard, run-of-the-mill Dell Office PC.
							</li>
						</ol>
