						<h2 id="picking">Picking the Parts</h2>
						This section is for understanding what parts to choose from based on their most important specs.
						 <h3>What to look for in a CPU</h3>
						When looking at the specs of CPUs, GHz are what you'll want to focus on. It indicates how many cycles a second are processed in that CPU. The more GHz it has the better it is.
						<h3>What to look for in a GPU</h3>
						When looking at the GPUs, you'll want to choose among the big brand names, Nvidia, AMD, and MSI. You'll also want to look out for the GPU's bandwidth compatibility memory size.
						<h3>What to look for in a Hard Drive</h3>
						Primarily, you'll want to focus on how much GBs(gigabytes) or TBs(terabytes) it could store. If you're building a gaming pc, then it would be better to get a lot of storage and a SSD(solid state drive) to load your games faster.
						<h3>What to look for in Motherboard</h3>
						It's important to see which slots a motherboard has and its shape to see if all your parts can fit onto the motherboard. Generally, it's better to have a newer model.
						<h3>What to look for in RAM</h3>
						When looking for RAM, you should choose based on its amount of storage. Typically, 16GB is enough.
						<h3>What to look for in a Power Supply</h3>
						You'll want to buy one that has a lot of wattage and voltage. However, you can optimize this by finding out how much power your PC would theoretically consume and buying a PSU that supplies that right amount.
